/**
 * T151(拡張ブック生成 フェーズ2/2、ステージ3): Edax評価結果を使って統合定石DAG
 * (`bookgen/joseki-research.json`+`bookgen/wthor-lines.json`)から
 * 「明白な悪手」を除外した対局専用の拡張ブック(`app/public/opening-book.json`)
 * を組み立てる純粋関数群。
 *
 * 手順(オーケストレーター確定の設計判断、`tasks/T151-book-eval-publish.md`参照。
 * v2、2026-07-20: 初版の一律除外は検収データで命名済み112ライン中69本が途中切断
 * (32本がply<=5)・除外135件中76件が閾値境界のロス2ちょうど、と定石カバレッジを
 * 削りすぎることが判明したため改定):
 * 1. 各bookMoveに、その局面の**全合法手**中の最善値との差(`loss`)を計算する
 *    (`resolveMoveValue`)。
 * 2. `loss >= lossThreshold`(既定2石)のbookMoveのうち、**命名済みライン
 *    (`bookgen/joseki-research.json`由来)の実際の手順に含まれない手だけ**を
 *    除外する(`filterOpeningBook`)。命名済みラインの手順に含まれる手は
 *    loss値に関わらず除外しない(`collectNamedLineMoveKeys`で判定した
 *    `protectedMoveKeys`、evalは通常どおり格納し、警告レポートには
 *    「高loss・保護により残存」として引き続き記載する)。
 * 3. 除外後に生存したbookMovesへ、`buildDb.ts`の`assignWeights`と同じロジックで
 *    重みを再割り当てする(頻度データが全件揃っていれば頻度比例、そうでなければ均等)。
 * 4. ルート局面(初期局面)から生存bookMovesだけを辿って到達可能なノードだけを
 *    残す(到達不能になったサブツリーを刈る、`pruneUnreachableNodes`)。
 *    命名済みラインの手は一切除外されないため、112ライン全ての経路が
 *    ルートからの到達可能性を保ち、必ず生存する。
 * 5. あるノードの全bookMovesが除外された場合、そのノードの`isLeaf`を`true`に
 *    補正する(2026-07-20レビュー指摘: bookMoves=[]なのに`isLeaf: false`のままだと
 *    `app.tsx`の`evaluateHumanMove`の`!josekiHit.isLeaf`判定が「定石」ソースだと
 *    誤判定してしまう不整合があった)。
 */

