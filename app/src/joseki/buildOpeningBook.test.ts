describe('resolveMoveValue', () => {
  it('terminal=trueならterminalValueをそのまま返す', () => {
    const value = resolveMoveValue(
      { nodeKey: 'n', move: 1, terminal: true, terminalValue: 12 },
      new Map(),
    )
    expect(value).toBe(12)
  })

  it('terminal=false・needsFlip=falseならpositionResultsの値をそのまま返す', () => {
    const value = resolveMoveValue(
      { nodeKey: 'n', move: 1, terminal: false, positionKey: 'p1', needsFlip: false },
      new Map([['p1', 5]]),
    )
    expect(value).toBe(5)
  })

  it('terminal=false・needsFlip=trueならpositionResultsの値を符号反転して返す', () => {
    const value = resolveMoveValue(
      { nodeKey: 'n', move: 1, terminal: false, positionKey: 'p1', needsFlip: true },
      new Map([['p1', 5]]),
    )
    expect(value).toBe(-5)
  })

  it('positionResultsに値が無ければ例外を投げる', () => {
    expect(() =>
      resolveMoveValue(
        { nodeKey: 'n', move: 1, terminal: false, positionKey: 'missing', needsFlip: true },
        new Map(),
      ),
    ).toThrow()
  })
})

describe('filterOpeningBook (v2: 命名済みラインの手はlossに関わらず除外しない)', () => {
  it('保護対象でなければ、lossThreshold以上のbookMoveを除外し、生存したbookMoveにevalを格納する', () => {
    const nodes = new Map([
      [
        'node1',
        makeNode([
          { move: 1, weight: 0, eval: null },
          { move: 2, weight: 0, eval: null },
          { move: 3, weight: 0, eval: null },
        ]),
      ],
    ])
    const moveValuesByNode = new Map([
      [
        'node1',
        new Map([
          [1, 10], // best
          [2, 8], // loss=2, 保護対象外 -> 除外
          [3, 9], // loss=1 -> 生存(閾値未満)
        ]),
      ],
    ])

    const { nodes: filtered, flagged } = filterOpeningBook(nodes, moveValuesByNode, 2, new Set())

    const node1 = filtered.get('node1')!
    expect(node1.bookMoves.map((bm) => bm.move).sort()).toEqual([1, 3])
    expect(node1.bookMoves.find((bm) => bm.move === 1)!.eval).toBe(10)
    expect(node1.bookMoves.find((bm) => bm.move === 3)!.eval).toBe(9)

    expect(flagged).toHaveLength(1)
    expect(flagged[0]).toMatchObject({
      nodeKey: 'node1',
      move: 2,
      loss: 2,
      bestValue: 10,
      moveValue: 8,
      protectedByNamedLine: false,
      excludedFromBook: true,
    })
  })

  it('v2の中核: 保護対象(命名済みラインの手順)ならlossThreshold以上でも除外しない', () => {
    const nodes = new Map([
      [
        'node1',
        makeNode([
          { move: 1, weight: 0, eval: null },
          { move: 2, weight: 0, eval: null }, // 命名済みラインの手として保護される想定
        ]),
      ],
    ])
    const moveValuesByNode = new Map([
      [
        'node1',
        new Map([
          [1, 10], // best
          [2, -10], // loss=20、非常に悪い手だが保護対象
        ]),
      ],
    ])
    const protectedMoveKeys = new Set([protectedMoveKey('node1', 2)])

    const { nodes: filtered, flagged } = filterOpeningBook(nodes, moveValuesByNode, 2, protectedMoveKeys)

    const node1 = filtered.get('node1')!
    // 除外されず、両方のbookMoveが生存する(evalは通常どおり格納)。
    expect(node1.bookMoves.map((bm) => bm.move).sort()).toEqual([1, 2])
    expect(node1.bookMoves.find((bm) => bm.move === 2)!.eval).toBe(-10)

    // 「高lossだった」という事実自体はflaggedに残る(警告レポート用)。
    expect(flagged).toHaveLength(1)
    expect(flagged[0]).toMatchObject({
      nodeKey: 'node1',
      move: 2,
      loss: 20,
      protectedByNamedLine: true,
      excludedFromBook: false,
    })
  })

  it('全bookMovesが除外されたノードはisLeafがtrueに補正される(元がfalseでも)', () => {
    const nodes = new Map([
      [
        'node1',
        makeNode(
          [
            { move: 1, weight: 0, eval: null },
            { move: 2, weight: 0, eval: null },
          ],
          ['ライン'],
          false, // 元はisLeaf: false(まだ続きがある局面のつもりだった)
        ),
      ],
    ])
    // 両方とも保護対象外・高loss -> どちらも除外され、bookMovesが空になる
    // (bestValueは、bookMove化されていない別の合法手99から来ている想定)。
    const moveValuesByNode = new Map([
      [
        'node1',
        new Map([
          [1, 3], // loss=7
          [2, 2], // loss=8
          [99, 10], // best(非bookMove)
        ]),
      ],
    ])

    const { nodes: filtered } = filterOpeningBook(nodes, moveValuesByNode, 2, new Set())
    const node1 = filtered.get('node1')!
    expect(node1.bookMoves).toEqual([])
    expect(node1.isLeaf).toBe(true)
  })

  it('bookMovesが1件以上生存すればisLeafは元の値のまま変わらない', () => {
    const nodes = new Map([['node1', makeNode([{ move: 1, weight: 0, eval: null }], ['ライン'], false)]])
    const moveValuesByNode = new Map([['node1', new Map([[1, 10]])]])
    const { nodes: filtered } = filterOpeningBook(nodes, moveValuesByNode, 2, new Set())
    expect(filtered.get('node1')!.isLeaf).toBe(false)
  })

  it('namedOriginは、ノードのnamesに自動命名(WTHOR-####)以外が1つでも含まれればtrue(保護判定とは独立)', () => {
    const nodes = new Map([
      ['namedNode', makeNode([{ move: 1, weight: 0, eval: null }], ['虎', 'WTHOR-0001'])],
      ['autoNode', makeNode([{ move: 1, weight: 0, eval: null }], ['WTHOR-0002'])],
    ])
    const moveValuesByNode = new Map([
      ['namedNode', new Map([[1, 0], [99, 10]])],
      ['autoNode', new Map([[1, 0], [99, 10]])],
    ])

    // どちらも保護集合には含めない(namedOriginは「ノードの合流」のゆるい目印であり、
    // 「この手自体が保護対象か」とは別の軸であることを確認する)。
    const { flagged } = filterOpeningBook(nodes, moveValuesByNode, 5, new Set())
    const named = flagged.find((e) => e.nodeKey === 'namedNode')!
    const auto = flagged.find((e) => e.nodeKey === 'autoNode')!
    expect(named.namedOrigin).toBe(true)
    expect(named.protectedByNamedLine).toBe(false)
    expect(named.excludedFromBook).toBe(true)
    expect(auto.namedOrigin).toBe(false)
  })

  it('moveValuesByNodeにノードのエントリが無ければそのノードは変更せずそのまま残す', () => {
    const node = makeNode([{ move: 1, weight: 0.5, eval: null }])
    const nodes = new Map([['orphan', node]])
    const { nodes: filtered, flagged } = filterOpeningBook(nodes, new Map(), 2, new Set())
    expect(filtered.get('orphan')).toBe(node)
    expect(flagged).toHaveLength(0)
  })

  it('bookMoveのmoveがmoveValuesByNodeに無ければ例外を投げる(全合法手を尽くしている前提の防御)', () => {
    const nodes = new Map([['node1', makeNode([{ move: 1, weight: 0, eval: null }])]])
    const moveValuesByNode = new Map([['node1', new Map([[2, 5]])]])
    expect(() => filterOpeningBook(nodes, moveValuesByNode, 2, new Set())).toThrow()
  })
})

