describe('collectNamedLineMoveKeys / checkNamedLineSurvival', () => {
  const rawLines: RawJosekiLine[] = [
    { name: 'A', moves: ['f5', 'f4', 'c3'], firstMoveBasis: 'f5', depth: 3 },
  ]

  it('collectNamedLineMoveKeysはラインの全手順(各局面,着手)をキー化する', () => {
    const keys = collectNamedLineMoveKeys(rawLines)
    expect(keys.size).toBe(3) // f5, f4, c3の3手

    let board = initialBoard()
    let side: Side = 'black'
    for (const notation of ['f5', 'f4', 'c3']) {
      const move = notationToSquare(notation)
      const key = hashBoard(board, side)
      expect(keys.has(protectedMoveKey(key, move)), `missing key for ${notation}`).toBe(true)
      board = applyMove(board, side, move)
      side = opposite(side)
    }
  })

  it('checkNamedLineSurvival: 全手順がbookDbに残っていればfullySurvived=true', () => {
    const db = buildJosekiDb(rawLines)
    const result = checkNamedLineSurvival(rawLines, db)
    expect(result).toEqual([{ name: 'A', totalMoves: 3, survivedMoves: 3, fullySurvived: true }])
  })

  it('checkNamedLineSurvival: 途中の手が無くなっていればfullySurvived=false', () => {
    const db = buildJosekiDb(rawLines)
    const f4Move = notationToSquare('f4')
    const afterF5Key = hashBoard(applyMove(initialBoard(), 'black', notationToSquare('f5')), 'white')
    const afterF5Node = db.nodes.get(afterF5Key)!
    const tampered = new Map(db.nodes)
    tampered.set(afterF5Key, { ...afterF5Node, bookMoves: afterF5Node.bookMoves.filter((bm) => bm.move !== f4Move) })

    const result = checkNamedLineSurvival(rawLines, { nodes: tampered, lines: db.lines })
    expect(result[0].fullySurvived).toBe(false)
    // f5・c3の2手は(各ステップが独立にチェックされるため)生存扱いのまま、
    // 途中のf4だけが生存カウントから外れる(f5-f4-c3の3手中2手)。
    expect(result[0].survivedMoves).toBe(2)
  })
})

describe('pruneUnreachableNodes', () => {
  // f5-f4-c3(ライン'A')とf5-d6-c4(ライン'B')の2ラインからDAGを構築し、
  // 「f5直後の局面」のbookMovesからd6を人為的に取り除いた(=悪手として
  // 除外されたことをシミュレートした)状態で、d6側のサブツリー
  // (d6直後の局面・c4直後の局面)だけが刈られることを確認する。
  const rawLines: RawJosekiLine[] = [
    { name: 'A', moves: ['f5', 'f4', 'c3'], firstMoveBasis: 'f5', depth: 3 },
    { name: 'B', moves: ['f5', 'd6', 'c4'], firstMoveBasis: 'f5', depth: 3 },
  ]
  const db = buildJosekiDb(rawLines)

  function keyAfter(notations: readonly string[]): string {
    let board = initialBoard()
    let side: Side = 'black'
    for (const n of notations) {
      board = applyMove(board, side, notationToSquare(n))
      side = opposite(side)
    }
    return hashBoard(board, side)
  }

  const rootKey = keyAfter([])
  const afterF5Key = keyAfter(['f5'])
  const afterF5F4Key = keyAfter(['f5', 'f4'])
  const afterF5F4C3Key = keyAfter(['f5', 'f4', 'c3'])
  const afterF5D6Key = keyAfter(['f5', 'd6'])
  const afterF5D6C4Key = keyAfter(['f5', 'd6', 'c4'])

  it('前提: フィルタ前のDAGは両ラインのノードをすべて持つ', () => {
    for (const key of [rootKey, afterF5Key, afterF5F4Key, afterF5F4C3Key, afterF5D6Key, afterF5D6C4Key]) {
      expect(db.nodes.has(key), `missing node for key derived from ${key}`).toBe(true)
    }
  })

  it('d6を除外(bookMovesから削除)すると、d6側のサブツリーだけが到達不能として刈られる', () => {
    const simulateFiltered = new Map(db.nodes)
    const afterF5Node = simulateFiltered.get(afterF5Key)!
    const d6Move = notationToSquare('d6')
    simulateFiltered.set(afterF5Key, {
      ...afterF5Node,
      bookMoves: afterF5Node.bookMoves.filter((bm) => bm.move !== d6Move),
    })

    const pruned = pruneUnreachableNodes(simulateFiltered)

    expect(pruned.has(rootKey)).toBe(true)
    expect(pruned.has(afterF5Key)).toBe(true)
    expect(pruned.has(afterF5F4Key)).toBe(true)
    expect(pruned.has(afterF5F4C3Key)).toBe(true)
    expect(pruned.has(afterF5D6Key)).toBe(false)
    expect(pruned.has(afterF5D6C4Key)).toBe(false)
  })

  it('何も除外しなければ全ノードが到達可能なまま残る', () => {
    const pruned = pruneUnreachableNodes(db.nodes)
    expect(pruned.size).toBe(db.nodes.size)
  })
})

describe('buildOpeningBookDb (統合、簡易な合成評価値で end-to-end 検証)', () => {
  const rawLines: RawJosekiLine[] = [
    { name: 'A', moves: ['f5', 'f4', 'c3'], firstMoveBasis: 'f5', depth: 3 },
    { name: 'B', moves: ['f5', 'd6', 'c4'], firstMoveBasis: 'f5', depth: 3 },
  ]
  const db = buildJosekiDb(rawLines)
  const { requests, positions } = collectMoveEvalRequests(db)
  // v2: このミニデータセットではAもBも「命名済みライン」扱い(WTHOR-####ではない)
  // なので、保護集合には両方の全手順を含める。フィルタの構造(重み正規化・
  // 到達可能性)を検証する目的では十分。
  const protectedMoveKeys = collectNamedLineMoveKeys(rawLines)

  // テスト用の合成評価値: OBF文字列中のXの数-Oの数(実際のEdax評価ではないが、
  // 決定的でrequests/positionsの配線を検証するには十分)。
  function syntheticScore(obf: string): number {
    let score = 0
    for (const c of obf) {
      if (c === 'X') score += 1
      else if (c === 'O') score -= 1
    }
    return score
  }
  const positionResults = new Map(positions.map((p) => [p.key, syntheticScore(p.board)]))

  it('flaggedの全件がloss>=lossThresholdを満たす', () => {
    const { flagged } = buildOpeningBookDb(db, requests, positionResults, 2, protectedMoveKeys)
    for (const f of flagged) {
      expect(f.loss).toBeGreaterThanOrEqual(2)
      expect(f.loss).toBe(f.bestValue - f.moveValue)
    }
  })

  it('AもBも命名済みラインとして保護されているため、このデータセットでは何も除外されない', () => {
    const { flagged } = buildOpeningBookDb(db, requests, positionResults, 2, protectedMoveKeys)
    expect(flagged.every((f) => f.protectedByNamedLine)).toBe(true)
    expect(flagged.every((f) => f.excludedFromBook === false)).toBe(true)
  })

  it('生存したbookMovesのevalはresolveMoveValueの結果と一致する', () => {
    const { db: result } = buildOpeningBookDb(db, requests, positionResults, 2, protectedMoveKeys)
    const moveValuesByNode = buildMoveValuesByNode(requests, positionResults)
    for (const [nodeKey, node] of result.nodes) {
      const moveValues = moveValuesByNode.get(nodeKey)
      if (!moveValues) continue
      for (const bookMove of node.bookMoves) {
        expect(bookMove.eval).toBe(moveValues.get(bookMove.move))
      }
    }
  })

  it('各ノードのbookMovesの重みは合計1になる(生存者がいる場合)', () => {
    const { db: result } = buildOpeningBookDb(db, requests, positionResults, 2, protectedMoveKeys)
    for (const node of result.nodes.values()) {
      if (node.bookMoves.length === 0) continue
      const total = node.bookMoves.reduce((sum, bm) => sum + bm.weight, 0)
      expect(total).toBeCloseTo(1, 10)
    }
  })

  it('刈り込み後の全ノードはルート(初期局面)から生存bookMovesを辿って到達可能', () => {
    const { db: result } = buildOpeningBookDb(db, requests, positionResults, 2, protectedMoveKeys)
    const rootKey = hashBoard(initialBoard(), 'black')
    expect(result.nodes.has(rootKey)).toBe(true)

    const reachable = new Set<string>([rootKey])
    const queue = [rootKey]
    while (queue.length > 0) {
      const key = queue.shift()!
      const node = result.nodes.get(key)!
      const { board, side } = parseNodeKey(key)
      for (const bm of node.bookMoves) {
        const childKey = hashBoard(applyMove(board, side, bm.move), opposite(side))
        if (result.nodes.has(childKey) && !reachable.has(childKey)) {
          reachable.add(childKey)
          queue.push(childKey)
        }
      }
    }

    expect(reachable.size).toBe(result.nodes.size)
    expect(result.nodes.size).toBeGreaterThan(0)
    expect(result.nodes.size).toBeLessThanOrEqual(db.nodes.size)
  })

  it('db.linesはそのまま素通しされる(フィルタ・刈り込みの対象外)', () => {
    const { db: result } = buildOpeningBookDb(db, requests, positionResults, 2, protectedMoveKeys)
    expect(result.lines).toBe(db.lines)
  })
})

describe('buildOpeningBookDb: 命名済み112ラインの完全生存(v2ポリシー、実データ+乱数風の敵対的評価値)', () => {
  // 実際のbookgen/joseki-research.json(112ライン)+wthor-lines.json(251ライン)を
  // 統合し、決定的だが「ランダムっぽい」合成評価値(局面キーのハッシュから導出)を
  // 与えることで、命名済みラインの手の一部も高loss(>=2)になるケースを意図的に
  // 発生させ、それでも除外されない(=保護ロジックが実データ規模で機能する)ことを
  // 確認する(2026-07-20オーケストレーター指摘: 命名済み112/112ラインの全生存確認)。
  const research = loadRawLines(RAW_JOSEKI_PATH)
  const wthor = loadRawLines(RAW_WTHOR_PATH)
  const rawLines = [...research, ...wthor]
  const db = buildJosekiDb(rawLines)
  const { requests, positions } = collectMoveEvalRequests(db)
  const protectedMoveKeys = collectNamedLineMoveKeys(research)

  function adversarialScore(key: string): number {
    let hash = 0
    for (let i = 0; i < key.length; i++) {
      hash = (hash * 31 + key.charCodeAt(i)) % 1000003
    }
    return (hash % 61) - 30 // -30..30の決定的な疑似乱数値
  }
  const positionResults = new Map(positions.map((p) => [p.key, adversarialScore(p.key)]))

  const { db: result, flagged } = buildOpeningBookDb(db, requests, positionResults, 2, protectedMoveKeys)

  it('保護対象(命名済みラインの手)は、lossが高くても実際には除外されない', () => {
    const protectedFlags = flagged.filter((f) => f.protectedByNamedLine)
    expect(protectedFlags.length).toBeGreaterThan(0) // この敵対的評価値では実際に高lossの保護対象手が発生する
    for (const f of protectedFlags) {
      expect(f.excludedFromBook).toBe(false)
    }
  })

  it('保護対象外(自動抽出ラインのみに乗る手)は高lossなら実際に除外される', () => {
    const excludedFlags = flagged.filter((f) => f.excludedFromBook)
    expect(excludedFlags.length).toBeGreaterThan(0)
    for (const f of excludedFlags) {
      expect(f.protectedByNamedLine).toBe(false)
    }
  })

  it('命名済み112ライン全ての手順が最終ブックに全て生存する(0/112の途中切断)', () => {
    const survival = checkNamedLineSurvival(research, result)
    expect(survival).toHaveLength(112)
    const notSurvived = survival.filter((r) => !r.fullySurvived)
    expect(notSurvived).toEqual([])
  })
})
