function loadRawLines(fixturePath: string): readonly RawJosekiLine[] {
  const raw = JSON.parse(readFileSync(fixturePath, 'utf-8')) as RawJosekiFile
  return raw.lines
}

function makeNode(bookMoves: JosekiBookMove[], names: string[] = ['ライン'], isLeaf = false): JosekiNode {
  return { bookMoves, nonBookEval: null, names, isLeaf }
}

